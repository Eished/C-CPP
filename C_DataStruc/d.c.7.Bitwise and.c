#include <stdio.h>
#include <malloc.h>
int main()
{
  // & | ^ ~ << >>
  int a = 4; // 00000000 00000000 00000000 00000100
  int b = 7; // 00000000 00000000 00000000 00000111
             // 00000000 00000000 00000000 00000100
  int C = a & b;
  printf("%d\n", C);

  // 迅速清零： 与 0 按位与
  int zero = a & 0;
  printf("%d\n", zero);

  // 保留指定位: b 低八位 置1, a&b, 得到 a 的低八位
  // 判断奇偶性: a&1=1 则是奇数，a&1=0 则是偶数
  return 0;
}
